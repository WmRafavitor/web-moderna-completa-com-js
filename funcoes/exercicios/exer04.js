// Crie uma função que irá receber dois valores, o dividendo e o divisor. A função
//deverá imprimir o resultado e o resto da divisão destes dois valores

function restDivision(dividend, divisor) {
    console.log(`o resultado da divisão é ${dividend / divisor}`);
    console.log(`o resto da divisão é: ${dividend % divisor}`);
}

restDivision(2, 4);